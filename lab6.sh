read -p "Enter the string " str
len=$(expr "$str" : '.*')
echo "the length of the string is $len"
sub=$(expr "$str" : '\(...\).*')
echo "the substring is $sub"
echo "enter the character to search:"
read ch
pos=$(expr "$str" : '[^'$ch']*'$ch)

if [ $pos -gt 0 ]
then 
echo "found at $pos"
else 
echo "character not found "
fi























































































































































































































































































